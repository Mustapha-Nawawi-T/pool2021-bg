int	ft_str_is_numeric(char *str)
{
	int	i;
	int	counter;

	i = 0;
	counter = 1;
	while (str[i] != '\0')
	{
		if (!(str[i] >= '0' && str[i] <= '9'))
		{
			counter = 0;
		}
		i++;
	}
	return (counter);
}
