int	ft_str_is_lowercase(char *str)
{
	int	i;
	int	counter;

	counter = 1;
	i = 0;
	while (str[i] != '\0')
	{
		if (!(str[i] >= 'a' && str[i] <= 'z'))
		{
			counter = 0;
		}
		i++;
	}
	return (counter);
}
